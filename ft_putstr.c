#include "ft_printf.h"

int ft_putstr(char *s)
{
	unsigned int	i;
    
	i = 0;
    if (!s)
    {
        ft_putstr("(null)");
        return (6);
    }
	while (s[i] != '\0')
	{
		ft_putchar(s[i]);
		i++;
	}
    return (i);
}
